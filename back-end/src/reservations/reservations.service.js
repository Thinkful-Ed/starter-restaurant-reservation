const knex = require('../db/connection');

function list(date) {
  
  return knex("reservations")
    .where({ reservation_date: date })
    .orderBy("reservation_time");
}

function create(newReservation) {
  return knex("reservations")
    .insert(newReservation)
    .returning("*")
    .then((createdRecords) => createdRecords[0]);
}



module.exports = {
	list,
  create
  
};
